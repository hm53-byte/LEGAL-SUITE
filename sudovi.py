def dohvati_sudove(vrsta=None):
    """Vraca listu naziva sudova, opcionalno filtriranu po vrsti.
    vrsta: 'opcinski', 'trgovacki', 'zupanijski', 'visoki', 'upravni', 'vrhovni'
    """
    if vrsta:
        return sorted([n for n, d in SUDOVI.items() if d["vrsta"] == vrsta])
    return sorted(SUDOVI.keys())


def dohvati_adresu_suda(naziv):
    """Vraca adresu suda po nazivu, ili prazan string."""
    sud = SUDOVI.get(naziv)
    return sud["adresa"] if sud else ""


def format_sud_s_dijakriticima(naziv):
    """Pretvara ASCII nazive sudova u nazive s hrvatskim dijakriticima za prikaz u dokumentu."""
    zamjene = {
        "OPCINSKI": "OPĆINSKI",
        "GRADANSKI": "GRAĐANSKI",
        "TRGOVACKI": "TRGOVAČKI",
        "ZUPANIJSKI": "ŽUPANIJSKI",
        "PREKRSAJNI": "PREKRŠAJNI",
        "SIBENIKU": "ŠIBENIKU",
        "GOSPIĆU": "GOSPIĆU",
        "DJAKOVU": "ĐAKOVU",
        "POZEGI": "POŽEGI",
        "CAZMI": "ČAZMI",
        "GRUBISNOM": "GRUBIŠNOM",
        "NASICAMA": "NAŠICAMA",
        "GRADISCI": "GRADIŠCI",
        "VARAZDINU": "VARAŽDINU",
    }
    rezultat = naziv
    for ascii_v, hrv in zamjene.items():
        rezultat = rezultat.replace(ascii_v, hrv)
    return rezultat


